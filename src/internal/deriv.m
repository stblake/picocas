(* Multiple variables and higher order *)
D[expr_, {x_, n_Integer}] /; n > 0 := D[D[expr, {x, n - 1}], x];
D[expr_, {x_, 0}] := expr;
D[expr_, x_, y__] := D[D[expr, x], y];

(* Basic structural rules for D *)
D[c_, x_] /; FreeQ[c, x] := 0;
D[x_, x_] := 1;
D[f_ + g_, x_] := D[f, x] + D[g, x];
D[f_ * g_, x_] := D[f, x] * g + f * D[g, x];
D[f_^g_, x_] := f^(g - 1) * (g * D[f, x] + f * Log[f] * D[g, x]);
D[Exp[f_], x_] := Exp[f] * D[f, x];
D[Log[f_], x_] := D[f, x] / f;
D[Log[b_, f_], x_] := D[Log[f]/Log[b], x];
D[Sin[f_], x_] := Cos[f] * D[f, x];
D[Cos[f_], x_] := -Sin[f] * D[f, x];
D[Tan[f_], x_] := Sec[f]^2 * D[f, x];
D[Cot[f_], x_] := -Csc[f]^2 * D[f, x];
D[Sec[f_], x_] := Sec[f] * Tan[f] * D[f, x];
D[Csc[f_], x_] := -Csc[f] * Cot[f] * D[f, x];
D[ArcSin[f_], x_] := D[f, x] / Sqrt[1 - f^2];
D[ArcCos[f_], x_] := -D[f, x] / Sqrt[1 - f^2];
D[ArcTan[f_], x_] := D[f, x] / (1 + f^2);
D[ArcCot[f_], x_] := -D[f, x] / (1 + f^2);
D[ArcSec[f_], x_] := D[f, x] / (f^2 * Sqrt[1 - 1/f^2]);
D[ArcCsc[f_], x_] := -D[f, x] / (f^2 * Sqrt[1 - 1/f^2]);
D[Sinh[f_], x_] := Cosh[f] * D[f, x];
D[Cosh[f_], x_] := Sinh[f] * D[f, x];
D[Tanh[f_], x_] := Sech[f]^2 * D[f, x];
D[Coth[f_], x_] := -Csch[f]^2 * D[f, x];
D[Sech[f_], x_] := -Sech[f] * Tanh[f] * D[f, x];
D[Csch[f_], x_] := -Csch[f] * Coth[f] * D[f, x];
D[ArcSinh[f_], x_] := D[f, x] / Sqrt[1 + f^2];
D[ArcCosh[f_], x_] := D[f, x] / Sqrt[f^2 - 1];
D[ArcTanh[f_], x_] := D[f, x] / (1 - f^2);
D[ArcCoth[f_], x_] := D[f, x] / (1 - f^2);
D[ArcSech[f_], x_] := -D[f, x] / (f^2 * Sqrt[1/f^2 - 1]);
D[ArcCsch[f_], x_] := -D[f, x] / (f^2 * Sqrt[1 + 1/f^2]);
D[f_[g_], x_] := Derivative[1][f][g] * D[g, x];
Dt[c_?NumberQ] := 0;
Dt[Pi] := 0;
Dt[E] := 0;
Dt[I] := 0;
Dt[Infinity] := 0;
Dt[ComplexInfinity] := 0;
Dt[f_ + g_] := Dt[f] + Dt[g];
Dt[f_ * g_] := Dt[f] * g + f * Dt[g];
Dt[f_^g_] := f^(g - 1) * (g * Dt[f] + f * Log[f] * Dt[g]);
Dt[Exp[f_]] := Exp[f] * Dt[f];
Dt[Log[f_]] := Dt[f] / f;
Dt[Log[b_, f_]] := Dt[Log[f]/Log[b]];
Dt[Sin[f_]] := Cos[f] * Dt[f];
Dt[Cos[f_]] := -Sin[f] * Dt[f];
Dt[Tan[f_]] := Sec[f]^2 * Dt[f];
Dt[Cot[f_]] := -Csc[f]^2 * Dt[f];
Dt[Sec[f_]] := Sec[f] * Tan[f] * Dt[f];
Dt[Csc[f_]] := -Csc[f] * Cot[f] * Dt[f];
Dt[ArcSin[f_]] := Dt[f] / Sqrt[1 - f^2];
Dt[ArcCos[f_]] := -Dt[f] / Sqrt[1 - f^2];
Dt[ArcTan[f_]] := Dt[f] / (1 + f^2);
Dt[ArcCot[f_]] := -Dt[f] / (1 + f^2);
Dt[ArcSec[f_]] := Dt[f] / (f^2 * Sqrt[1 - 1/f^2]);
Dt[ArcCsc[f_]] := -Dt[f] / (f^2 * Sqrt[1 - 1/f^2]);
Dt[Sinh[f_]] := Cosh[f] * Dt[f];
Dt[Cosh[f_]] := Sinh[f] * Dt[f];
Dt[Tanh[f_]] := Sech[f]^2 * Dt[f];
Dt[Coth[f_]] := -Csch[f]^2 * Dt[f];
Dt[Sech[f_]] := -Sech[f] * Tanh[f] * Dt[f];
Dt[Csch[f_]] := -Csch[f] * Coth[f] * Dt[f];
Dt[ArcSinh[f_]] := Dt[f] / Sqrt[1 + f^2];
Dt[ArcCosh[f_]] := Dt[f] / Sqrt[f^2 - 1];
Dt[ArcTanh[f_]] := Dt[f] / (1 - f^2);
Dt[ArcCoth[f_]] := Dt[f] / (1 - f^2);
Dt[ArcSech[f_]] := -Dt[f] / (f^2 * Sqrt[1/f^2 - 1]);
Dt[ArcCsch[f_]] := -Dt[f] / (f^2 * Sqrt[1 + 1/f^2]);
Dt[f_[g_]] := Derivative[1][f][g] * Dt[g];
Dt[f_, x_] := D[f, x];
Dt[f_, {x_, n_Integer}] := D[f, {x, n}];
Dt[f_, x_, y__] := Dt[Dt[f, x], y];
SetAttributes[D, {Listable, Protected, ReadProtected}];
SetAttributes[Dt, {Listable, Protected, ReadProtected}];
SetAttributes[Derivative, {Protected, ReadProtected}]